function updateActiveButton(lang) {
    console.log(`Updating active button for language: ${lang}`);
    document.querySelectorAll('button').forEach(button => {
        button.classList.remove('active');
    });
    const activeButton = document.getElementById(lang + 'Btn');
    if (activeButton) {
        activeButton.classList.add('active');
    } else {
        console.log(`Button not found for language: ${lang}`);
    }
}

// Restore the language on page load
window.addEventListener("load", () => {
    const savedLang = localStorage.getItem('language') || 'en'; // default to English
    switchLanguage(savedLang);
});

