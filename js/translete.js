// Функция для переключения языка
function switchLanguage(lang) {
    console.log(`Switching to language: ${lang}`);
    localStorage.setItem('language', lang); // Сохраняем выбранный язык
    selectedLang = lang;  // Сохраняем текущий язык
    updateTexts(lang); // Обновляем текстовые элементы
    updateActiveButton(lang); // Обновляем активную кнопку

    // Переинициализируем Owl Carousel после обновления текста
    $(".testimonial-carousel").owlCarousel('destroy'); // Уничтожаем старую инициализацию
    $(".testimonial-carousel").owlCarousel({  // Переинициализируем
        autoplay: false,
        smartSpeed: 1000,
        items: 1,
        dots: false,
        loop: true,
        nav: true,
        navText: [
            '<i class="bi bi-arrow-left"></i>',
            '<i class="bi bi-arrow-right"></i>'
        ],
    });

    // Переходим к следующему слайду
    $(".testimonial-carousel").trigger('next.owl.carousel');
}

